-- 테이블 생성--

-- 멤버테이블 --
CREATE TABLE MEMBER (
	MEM_NUM NUMBER PRIMARY KEY,
	MEM_ID VARCHAR2(20) UNIQUE NOT NULL,
	MEM_PWD VARCHAR2(20) NOT NULL,
	MEM_NAME VARCHAR2(20) NOT NULL,
	EMAIL VARCHAR2(30) UNIQUE NOT NULL,
	PHONE VARCHAR2(11) NOT NULL,
	POST VARCHAR2(10) NOT NULL,
    ADDRESS VARCHAR2(80) NOT NULL,
	REC_NUM NUMBER NULL,
	ENROLL_DATE DATE DEFAULT SYSDATE,
	STATUS VARCHAR2(2) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N'))
);

-- 쿠폰테이블 --
CREATE TABLE COUPON (
	COU_NUM NUMBER PRIMARY KEY,
	MEM_NUM NUMBER NOT NULL,
	COU_TYPE VARCHAR2(30) NOT NULL,
	ISSUE_DATE DATE DEFAULT SYSDATE,
	EXP_DATE DATE NULL,
	DISCOUNT NUMBER NOT NULL,
	STATUS VARCHAR2(2) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N')),
    FOREIGN KEY(MEM_NUM) REFERENCES MEMBER(MEM_NUM)
);

-- 카드테이블 --
CREATE TABLE CARD (
	CARD_ID NUMBER PRIMARY KEY,
	MEM_NUM NUMBER NOT NULL,
	CARD_NUM VARCHAR2(20) UNIQUE NOT NULL,
	CARD_COM VARCHAR2(20) NOT NULL CHECK (CARD_COM IN('신한', '우리', '국민', '농협')),
	CARD_CVC NUMBER NOT NULL,
	EXP_DATE DATE NOT NULL,
	STATUS VARCHAR2(2) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N')),
    FOREIGN KEY(MEM_NUM) REFERENCES MEMBER(MEM_NUM)
);

-- 문의테이블 --
CREATE TABLE ASK (
	ASK_NUM NUMBER PRIMARY KEY,
	MEM_NUM NUMBER NOT NULL,
	ASK_DATE DATE DEFAULT SYSDATE,
	ASK_TYPE VARCHAR2(20) NOT NULL,
	ASK_TITLE VARCHAR2(100) NOT NULL,
	ASK_CONTENT VARCHAR2(500) NOT NULL,
	ANS_YN VARCHAR2(2) DEFAULT 'N' CHECK (ANS_YN IN('Y', 'N')),
	ANS_CONTENT VARCHAR2(500) NULL,
	ANS_DATE DATE NULL,
    FOREIGN KEY(MEM_NUM) REFERENCES MEMBER(MEM_NUM)
);

-- 구독테이블 --
CREATE TABLE SUBSCRIBE (
	SUB_NUM NUMBER PRIMARY KEY,
	MEM_NUM NUMBER NOT NULL,
	SUB_OPTION VARCHAR2(20) NOT NULL,
	SUB_SDATE DATE DEFAULT SYSDATE,
	SUB_EDATE DATE NULL,
	PRICE NUMBER NOT NULL,
	SUB_DAY VARCHAR2(10) NOT NULL,
	STATUS VARCHAR2(2) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N')),
    FOREIGN KEY(MEM_NUM) REFERENCES MEMBER(MEM_NUM)
);

-- 예약테이블 -- 
CREATE TABLE RESERVE (
	RES_NUM NUMBER PRIMARY KEY,
	MEM_NUM NUMBER NOT NULL,
	RES_TYPE VARCHAR2(6) NOT NULL CHECK (RES_TYPE IN('세탁', '수선')),
	RES_DATE DATE DEFAULT SYSDATE,
	PICKUP_DATE DATE NOT NULL,
	REQUEST VARCHAR2(200) NULL,
	DEL_DATE DATE NULL,
	RES_STATUS VARCHAR2(2) DEFAULT 'N' CHECK (RES_STATUS IN('Y', 'N')),
	PAY_STATUS VARCHAR2(2) DEFAULT 'N' CHECK (PAY_STATUS IN('Y', 'N')),
	TOTAL_PAY NUMBER NOT NULL,
    FOREIGN KEY(MEM_NUM) REFERENCES MEMBER(MEM_NUM)
);

-- 세탁카테고리테이블 --
CREATE TABLE LCATEGORY (
	CAT_NUM NUMBER PRIMARY KEY,
	MCATEGORY VARCHAR2(20) NOT NULL,
	SCATEGORY VARCHAR2(40) UNIQUE NOT NULL,
	L_PRICE NUMBER NOT NULL,
	STATUS VARCHAR2(2) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N'))
);


-- 세탁테이블 --
CREATE TABLE LAUNDRY (
	LAU_NUM NUMBER PRIMARY KEY,
    RES_NUM NUMBER NOT NULL,
	CAT_NUM NUMBER NOT NULL,
    FOREIGN KEY(RES_NUM) REFERENCES RESERVE(RES_NUM),
    FOREIGN KEY(CAT_NUM) REFERENCES LCATEGORY(CAT_NUM)
);

-- 수선테이블 --
CREATE TABLE REPAIR (
	REP_NUM NUMBER PRIMARY KEY,
	RES_NUM NUMBER NOT NULL,
	R_CATEGORY VARCHAR2(20) NOT NULL,
	LENGTH_YN VARCHAR2(2) DEFAULT 'N' CHECK (LENGTH_YN IN('Y', 'N')),
	WIDTH_YN VARCHAR2(2) DEFAULT 'N' CHECK (WIDTH_YN IN('Y', 'N')),
	RESEAL_YN VARCHAR2(2) DEFAULT 'N' CHECK (RESEAL_YN IN('Y', 'N')),
	ZIP_YN VARCHAR2(2) DEFAULT 'N' CHECK (ZIP_YN IN('Y', 'N')),
	R_PRICE NUMBER NOT NULL,
    FOREIGN KEY(RES_NUM) REFERENCES RESERVE(RES_NUM)
);

-- 후기테이블 --
CREATE TABLE REVIEW (
	REV_NUM NUMBER PRIMARY KEY,
	RES_NUM NUMBER NOT NULL,
	REV_DATE DATE DEFAULT SYSDATE,
	REV_CONTENT VARCHAR2(500) NOT NULL,
	RATE VARCHAR2(10) NOT NULL,
	STATUS VARCHAR2(2) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N')),
    FOREIGN KEY(RES_NUM) REFERENCES RESERVE(RES_NUM)
);

-- 사진테이블 --
CREATE TABLE ATTACHMENT (
	ATT_NUM NUMBER PRIMARY KEY,
	ORIGIN_NAME VARCHAR2(255) NOT NULL,
	CHANGE_NAME VARCHAR2(255) NOT NULL,
	FILE_PATH VARCHAR2(1000) NOT NULL,
	UPLOAD_DATE DATE DEFAULT SYSDATE,
	STATUS VARCHAR2(2) DEFAULT 'Y' CHECK (STATUS IN('Y', 'N')),
	REV_NUM NUMBER NULL,
	ASK_NUM NUMBER NULL,
    FOREIGN KEY(REV_NUM) REFERENCES REVIEW(REV_NUM),
    FOREIGN KEY(ASK_NUM) REFERENCES ASK(ASK_NUM)
);

-- 결제테이블 --
CREATE TABLE PAYMENT (
	PAY_NUM NUMBER PRIMARY KEY,
	SUB_NUM NUMBER NULL,
	RES_NUM NUMBER NULL,
	COU_NUM NUMBER NULL,
	CARD_ID NUMBER NOT NULL,
    PAY_DATE DATE DEFAULT SYSDATE,
	REFUND VARCHAR2(2) DEFAULT 'N' CHECK (REFUND IN('Y', 'N')),
    STATUS VARCHAR2(2) DEFAULT 'N' CHECK (STATUS IN('Y', 'N')),
    FOREIGN KEY(SUB_NUM) REFERENCES SUBSCRIBE(SUB_NUM),
    FOREIGN KEY(RES_NUM) REFERENCES RESERVE(RES_NUM),
    FOREIGN KEY(COU_NUM) REFERENCES COUPON(COU_NUM),
    FOREIGN KEY(CARD_ID) REFERENCES CARD(CARD_ID)
);

